
#in Break statement if condition met it will stop
for i in range(11):
    if(i == 8):
      break
    print("5 *" , i , "=" , 5 * i )


#in continue statement if condition met it will not execute that condition and continues
for i in range(11):
    if(i == 8):
      continue
    print("5 *" , i , "=" , 5 * i )